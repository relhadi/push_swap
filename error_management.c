int	error_management(char *str)
{
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (!((*str == '-' || *str == '+')
			|| (*str >= '0' && *str <= '9')))
		return (1);
	if ((*str == '-' || *str == '+')
		&& (!(*(str + 1) >= '0' && *(str + 1) <= '9')))
		return (1);
	if (*str == '-' || *str == '+')
		str++;
	while (*str)
	{
		if (!(*str >= '0' && *str <= '9'))
			return (1);
		str++;
	}
	return (0);
}
